package org.yi.happy.archive;

public class ShortBodyException extends VerifyException {

    /**
     * 
     */
    private static final long serialVersionUID = 4112949691704097245L;

    public ShortBodyException() {
	super();
    }

    public ShortBodyException(String message, Throwable cause) {
	super(message, cause);
    }

    public ShortBodyException(String message) {
	super(message);
    }

    public ShortBodyException(Throwable cause) {
	super(cause);
    }

}
