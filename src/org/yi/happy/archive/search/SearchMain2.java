    /**
     * Find the first line in the file that is greater than or equal to the
     * target line, using a binary search.
     * 
     * @param file
     *            the file to search.
     * @param target
     *            the target to find.
     * @param cache
     * @return the first line that is greater than or equal to the target line.
     * @throws IOException
     */
    public static long searchBinary(RandomLineReader file, String target)
            throws IOException {
        String line;

        line = file.getFirst();
        if (line == null || line.compareTo(target) >= 0) {
            /*
             * the start is too far
             */
            return 0;
        }

        // long beg = file.getFilePointer() - 1;
        long beg = 0;
        long end = file.length();
        while (beg <= end) {
            long mid = beg + (end - beg) / 2;
            line = file.get(mid);

            if (line == null || line.compareTo(target) >= 0) {
                /*
                 * maybe went too far
                 */
                end = mid - 1;
            } else {
                /*
                 * not far enough
                 */
                beg = mid + 1;
            }
        }

        return file.getPosition(beg);
    }
